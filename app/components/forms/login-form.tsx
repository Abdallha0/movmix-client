"use client"
import Link from "next/link"
import "./css/login-styles.css"
import { Eye, EyeClosed } from "lucide-react"
import { FormEvent, useEffect, useState } from "react"
import loginValidator from "./utils/login-validators"
import login from "@/app/api/login"
import { useToast } from "@/app/providers/toastProvider"
import { setImage, setToken, setUserName } from "@/app/utils/cookieUtils"
import { useRouter } from "next/navigation"
import { signIn } from 'next-auth/react';
import AOS from "aos";
import "aos/dist/aos.css";

function Login() {

  useEffect(() => { AOS.init({ duration: 800, once: true }) }, []);
  const [rememberMe, setRememberMe] = useState(false)

  const [loading, setLoading] = useState(false);
  const [showPass, setShowPass] = useState(false);
  const [validateMsg, setValidateMsg] = useState({
    to: "",
    message: ""
  })

  const { showToast } = useToast();
  const router = useRouter();

  async function handleSubmition(e: FormEvent<HTMLFormElement>) {
    e.preventDefault();
    setLoading(true);

    // get form data
    const user_data = new FormData(e.currentTarget)
    const [email, password] = [
      user_data.get("email") as string,
      user_data.get("password") as string
    ];

    // validate data
    const validate_data = loginValidator(email, password)
    if (validate_data !== "success") {
      setLoading(false);
      setValidateMsg(validateMsg);
      return;
    }

    // send request to server
    const res = await login(email, password);

    if (!res?.status) {
      setLoading(false)
      showToast(res?.message, "error");
      return;
    }

    setToken(res.data.token);
    setUserName(res.data.name || "");
    if (!res.data.photo && typeof window !== "undefined") {
      window.sessionStorage.setItem('form-allaw', "allow");
    } else {
      sessionStorage.removeItem('form-allaw')
      setImage(res.data.photo || "/profile.png")
    }


    router.push("/home")
  }

  if (loading) return <div className="loading"></div>

  return (
    <div className="login-container" data-aos="zoom-in">
      <div className="login-card">
        <div className="logo-section">
          <div className="logo">
            <div className="logo-icon">
              M
            </div>
            <h1 className="logo">
              Movmix
            </h1>
          </div>
          <p className="tagline">Stream unlimited movies</p>
        </div>
        <form action="" method="post" onSubmit={handleSubmition} className="login-form" noValidate>

          <div className="login-form">
            <label className="form-label" htmlFor="email">{validateMsg.to === "email" ? validateMsg.message : "Email"}</label>
            <input type="text" className="form-input" name="email" id="email" />
          </div>

          <div className="form-group">
            <label className="form-label" htmlFor="password">{validateMsg.to === "password" ? validateMsg.message : "Password"}</label>
            <input type={!showPass ? "password" : "text"} className="form-input" name="password" id="password" />
            <button
              type="button"
              className="password-toggle"
              onClick={() => setShowPass(!showPass)}
              aria-label={showPass ? "Hide password" : "Show password"}
            >
              {showPass ? (
                <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                  <path d="M17.94 17.94A10.07 10.07 0 0 1 12 20c-7 0-11-8-11-8a18.45 18.45 0 0 1 5.06-5.94M9.9 4.24A9.12 9.12 0 0 1 12 4c7 0 11 8 11 8a18.5 18.5 0 0 1-2.16 3.19m-6.72-1.07a3 3 0 1 1-4.24-4.24" />
                  <line x1="1" y1="1" x2="23" y2="23" />
                </svg>
              ) : (
                <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                  <path d="M1 12s4-8 11-8 11 8 11 8-4 8-11 8-11-8-11-8z" />
                  <circle cx="12" cy="12" r="3" />
                </svg>
              )}
            </button>
          </div>
          <div className="form-options">
            <label className="remember-me">
              <input type="checkbox" checked={rememberMe} onChange={() => setRememberMe(!rememberMe)} />
              <span className="checkbox-custom"></span>
              <span>Remember me</span>
            </label>
            <Link href="/forget-password" className="forgot-link">
              Forgot Password?
            </Link>
          </div>

          <button type="submit" className="submit-btn">
            Sign In
          </button>
        </form>
        <div className="divider">
          <span className="divider-line"></span>
          <span className="divider-text">or continue with</span>
          <span className="divider-line"></span>
        </div>

        {/* Social Login */}
        <div className="social-login">
          <button type="button" className="social-btn" onClick={() => signIn("google")}>
            <svg viewBox="0 0 24 24" fill="currentColor">
              <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
              <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
              <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
              <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
            </svg>
            Google
          </button>
          <button type="button" className="social-btn">
            <svg viewBox="0 0 24 24" fill="currentColor">
              <path d="M12 2C6.477 2 2 6.477 2 12c0 4.42 2.865 8.166 6.839 9.489.5.092.682-.217.682-.482 0-.237-.008-.866-.013-1.7-2.782.604-3.369-1.341-3.369-1.341-.454-1.155-1.11-1.462-1.11-1.462-.908-.62.069-.608.069-.608 1.003.07 1.531 1.03 1.531 1.03.892 1.529 2.341 1.087 2.91.832.092-.647.35-1.088.636-1.338-2.22-.253-4.555-1.11-4.555-4.943 0-1.091.39-1.984 1.029-2.683-.103-.253-.446-1.27.098-2.647 0 0 .84-.269 2.75 1.025A9.578 9.578 0 0 1 12 6.836c.85.004 1.705.114 2.504.336 1.909-1.294 2.747-1.025 2.747-1.025.546 1.377.203 2.394.1 2.647.64.699 1.028 1.592 1.028 2.683 0 3.842-2.339 4.687-4.566 4.935.359.309.678.919.678 1.852 0 1.336-.012 2.415-.012 2.743 0 .267.18.578.688.48C19.138 20.163 22 16.418 22 12c0-5.523-4.477-10-10-10z" />
            </svg>
            GitHub
          </button>
        </div>

        <div className="signup-section">
          <p>
            New to Movmix?
            <Link href="/register" className="signup-link">
              Create an account
            </Link>
          </p>
        </div>
      </div>
    </div>
  )
}

export default Login
